#define sampleFreq	50.0f		// sample frequency in Hz
#define betaDef		0.1f		// 2 * proportional gain

//---------------------------------------------------------------------------------------------------
// Variable definitions

volatile double beta = betaDef;                                // 2 * proportional gain (Kp)

//---------------------------------------------------------------------------------------------------
